-module(teal).

-export([assert/3]).

%%%===================================================================
%%% API
%%%===================================================================

assert(Lhs, Rhs, Message) ->
    try Lhs = Rhs of
        Lhs -> true
    catch
        error:{badmatch, _} ->
            erlang:error(Message)
    end.

